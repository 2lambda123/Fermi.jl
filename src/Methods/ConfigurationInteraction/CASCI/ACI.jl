function CASCI{T}(refwfn::Fermi.HartreeFock.RHF, h::Array{T,2}, V::Array{T,4}, frozen::Int, act_elec::Int, active::Int, Alg::ACI; ci=nothing) where T <: AbstractFloat

    # Print intro
    Fermi.ConfigurationInteraction.print_header()
    ttotal = @elapsed begin
    @output "\n    ‚Ä¢ Computing FCI with the ACI algorithm.\n\n"
    act_range = (frozen+1):(active+frozen)
    œÉ = Fermi.CurrentOptions["œÉ"]
    Œ≥ = Fermi.CurrentOptions["Œ≥"]
    pe = Fermi.CurrentOptions["aci_print_screen"]

    @output "\n ‚Üí  ACTIVE SPACE\n"
    @output "Frozen Orbitals:  {:3d}\n" frozen
    @output "Active Electrons: {:3d}\n" act_elec
    @output "Active Orbitals:  {:3d}\n" active

    # Start reference space as HF
    zeroth = repeat('1', frozen)*repeat('1', Int(act_elec/2))
    if ci == nothing
        P = [Determinant(zeroth, zeroth)]
        Pcoef = [1.0]
    else
        P = deepcopy(ci.dets)
        Pcoef = deepcopy(ci.coef)
    end
    E = refwfn.energy - refwfn.molecule.Vnuc
    ŒîE = 1.0
    ite = 1

    @output repeat("=",50)*"\n"
    NŒ± = Int(act_elec/2)
    NŒ≤ = Int(act_elec/2)
    Lenny = length(P)
    M = nothing
    œµsum = nothing
    œµest = nothing
    oldP = nothing
    cflag = true
    while true
        if ite > 20
            break
        end
        @output " ‚Üí Iteration {}\n\n" ite
        @output "   ‚Ä¢ P\n"
        @output "Initial model space size: {}\n\n" length(P)

        @output "   ‚Ä¢ P ‚áí F\n"
        @output "Generating First Order Interacting Space...\n"
        t = @elapsed F = get_fois(P, NŒ±, NŒ≤, act_range)
        @output "FOIS size:                {}\n" length(F)
        @output "FOIS contructed in {:5.5f} s.\n\n" t

        @output "   ‚Ä¢ F ‚áí Q\n"
        @output "Screening FOIS using 2-D Hamiltonian\n" œÉ
        t = @elapsed Fe = œµI(F, P, Pcoef, E, h, V)
        @output "Screen complete in {:5.5} s.\n" t
        @output "Sorting F space...\n"
        _t = @elapsed begin
            Fperm = zeros(Int, length(Fe))
            sortperm!(Fperm, Fe, by=abs)
            reverse!(Fperm)
            Fe = Fe[Fperm]
            F = F[Fperm]
        end
        @output "Sorted in {:5.5f} s.\n" _t
        @output "Filtering F..."
        t = @elapsed begin
        œµest = 0.0
        œµsum = 0.0
        while true
            if length(Fe) == 0 
                #then no determinants were deemed important - exit ACI
                break
            end
            if œµsum ‚â§ œÉ
                œµest += Fe[end]
                œµsum += abs(Fe[end])
                pop!(Fe)
                pop!(F)
            else
                œµest -= Fe[end]
                œµsum -= abs(Fe[end])
                break
            end
        end
        end
        @output " Secondary space (Q) built in {:5.5f}\n\n" t
        @output "Size of Q {}\n" length(Fe)

        @output "   ‚Ä¢ M = P ‚à™ Q\n"
        ŒîE = -E
        M = vcat(P, F)
        @output "Model space size: {}\n" length(M)
        @output "Updating model space ..." length(M)
        t = @elapsed E, Pcoef, P = update_model_space(M, h, V)
        @output " Model space updated in {:5.5f}\n" t
        ŒîE += E
        @output "Model Space Energy           {:15.10f}\n" E + refwfn.molecule.Vnuc
        @output "Energy Change                {:15.10f}\n" ŒîE

        if oldP == Set(P) 
            break
        end
        oldP = Set(deepcopy(P))
        Lenny = length(P)
        @output "Coarse graining model space for next iteration\n"
        # Coarse grain
        Cperm = zeros(Int, length(P))
        sortperm!(Cperm, Pcoef, by=i->i^2)
        reverse!(Cperm)

        Pcoef = Pcoef[Cperm]
        P = P[Cperm]

        while true
            if sum(Pcoef[1:end-1].^2) >= 1-Œ≥*œÉ
                pop!(Pcoef)
                pop!(P)
            else
                break
            end
        end
        @output "Final coarse grained model space size is {}\n" length(P)
        @output repeat("=",50)*"\n"
        ite += 1
    end
    end #@elapsed

    @output repeat("=",50)*"\n"
    if cflag
        @output "üî•üî•üî•üî•üî• ACI procedure has converged. üî•üî•üî•üî•üî•\n"
    else
        @output "üò≤üò≤üò≤üò≤üò≤ ACI procedure has failed!!!! üò≤üò≤üò≤üò≤üò≤\n"
    end
    @output "Computation finished in {:5.5} seconds.\n" ttotal
    @output "Model space size: {}\n" length(M)
    @output "E[ACI:{}]     = {:15.10f}\n" œÉ E + refwfn.molecule.Vnuc
    @output "E[ACI:{}]+PT2 = {:15.10f}\n" œÉ E + refwfn.molecule.Vnuc + œµest
    @output repeat("=",51)*"\n\n"
    E = (E+refwfn.molecule.Vnuc)

    @output "\n ‚Ä¢ Most important determinants:\n\n"

    for i in 1:(min(20,length(P)))
        @output "{:15.5f}      {}\n" Pcoef[i]  detstring(P[i], frozen+active)
    end
    
    CASCI{T}(refwfn, E, P, Pcoef)
end

@fastmath @inbounds function get_fois(dets::Array{Determinant,1}, NŒ±::Int, NŒ≤::Int, act_range::UnitRange{Int64})::Array{Determinant,1}

    # Ns must be > 1
    Œ±occs = [zeros(Int,NŒ±) for i=1:Threads.nthreads()]
    Œ≤occs = [zeros(Int,NŒ≤) for i=1:Threads.nthreads()]
    Œ±unos = [zeros(Int,length(act_range)-NŒ±) for i=1:Threads.nthreads()]
    Œ≤unos = [zeros(Int,length(act_range)-NŒ≤) for i=1:Threads.nthreads()]

    lf_per_det = (length(Œ±occs[1])^2*length(Œ±unos[1])^2 + length(Œ±occs[1])*length(Œ±unos[1])
                       + length(Œ≤occs[1])^2*length(Œ≤unos[1])^2 + length(Œ≤occs[1])*length(Œ≤unos[1])
                       + length(Œ±occs[1])*length(Œ±unos[1])*length(Œ≤occs[1])*length(Œ≤unos[1]))
    lf_crit = Int(round(length(dets)*lf_per_det))
    fois = [Determinant(0,0) for i=1:lf_crit]
    @sync for _DI in eachindex(dets)
        Threads.@spawn begin
            d = dets[_DI]
            DI = (_DI-1)*lf_per_det + 1
            ct = 0
            id = Threads.threadid()
            Œ±occ = Œ±occs[id]
            Œ≤occ = Œ≤occs[id]
            Œ±uno = Œ±unos[id]
            Œ≤uno = Œ≤unos[id]
            Œ±occ!(d, act_range, Œ±occ)
            Œ≤occ!(d, act_range, Œ≤occ)
            Œ±vir!(d, act_range, Œ±uno)
            Œ≤vir!(d, act_range, Œ≤uno)
            # Get Œ±Œ± -> Œ±Œ± excitations
            for i in Œ±occ
                for a in Œ±uno
                    newŒ± = (d.Œ± ‚äª (1<<(i-1))) | (1<<(a-1)) 
                    _det = Determinant(newŒ±, d.Œ≤)
                    fois[DI+ct] = _det
                    ct += 1
                    for j in Œ±occ
                        if j ‚â• i
                            break
                        end
                        for b in Œ±uno
                            if b ‚â• a
                                break
                            end
                            newestŒ± = (newŒ± ‚äª (1<<(j-1))) | (1<<(b-1)) 
                            _det = Determinant(newestŒ±, d.Œ≤)
                            fois[DI+ct] = _det
                            ct += 1
                        end
                    end
                end
            end
            # Get Œ≤Œ≤ -> Œ≤Œ≤ excitations
            for i in Œ≤occ
                for a in Œ≤uno
                    newŒ≤ = (d.Œ≤ ‚äª (1<<(i-1))) | (1<<(a-1)) 
                    _det = Determinant(d.Œ±, newŒ≤)
                    fois[DI+ct] = _det
                    ct += 1
                    for j in Œ≤occ
                        if j ‚â• i
                            break
                        end
                        for b in Œ≤uno
                            if b ‚â• a
                                break
                            end
                            newestŒ≤ = (newŒ≤ ‚äª (1<<(j-1))) | (1<<(b-1)) 
                            _det = Determinant(d.Œ±, newestŒ≤)
                            fois[DI+ct] = _det
                            ct += 1
                        end
                    end
                end
            end
            # Get Œ±Œ≤ -> Œ±Œ≤ excitations
            for i in Œ±occ
                for a in Œ±uno
                    for j in Œ≤occ
                        for b in Œ≤uno
                            newŒ± = (d.Œ± ‚äª (1<<(i-1))) | (1<<(a-1)) 
                            newŒ≤ = (d.Œ≤ ‚äª (1<<(j-1))) | (1<<(b-1)) 
                            _det = Determinant(newŒ±, newŒ≤)
                            fois[DI+ct] = _det
                            ct += 1
                        end
                    end
                end
            end
        end #Threads.@spawn 
    end
    fois = filter((x)->x != Determinant(0,0), fois)
    fois = Set(fois)
    setdiff!(fois, dets)
    fois = collect(fois)
    return fois
end

@fastmath @inbounds function œµI(Fdets, P::Array{Determinant,1}, Pcoef::Array{Float64,1}, Ep::T, h::Array{T,2}, V::Array{T,4}) where T <: AbstractFloat
    Fe = zeros(length(Fdets))
    N = sum(Œ±list(P[1]))
    Œ±inds = [Array{Int64,1}(undef,N) for i=1:Threads.nthreads()]
    Œ≤inds = [Array{Int64,1}(undef,N) for i=1:Threads.nthreads()]
    @sync for i in eachindex(Fdets)
        Threads.@spawn begin
        D1 = Fdets[i]
        id = Threads.threadid()
        Œ±ind = Œ±inds[id]
        Œ≤ind = Œ≤inds[id]
        Œ±index!(D1, Œ±ind)
        Œ≤index!(D1, Œ≤ind)
        Ei = Hd0(Œ±ind, Œ≤ind, h, V)
        Œî = Ei - Ep
        Vint = 0.0
        for j in eachindex(P)
            D2 = P[j]
            Œ±exc = Œ±excitation_level(D1,D2)
            Œ≤exc = Œ≤excitation_level(D1,D2)
            el = Œ±exc + Œ≤exc
            if el > 2
                continue 
            elseif el == 2
                Vint += Pcoef[j]*Hd2(D1, D2, V, Œ±exc)
            elseif el == 1
                Vint += Pcoef[j]*Hd1(Œ±ind, Œ≤ind, D1, D2, h, V, Œ±exc)
            end
        end
        
        @fastmath Fe[i] = Œî/2 - ‚àö((Œî^2)/4 + Vint^2)
        end #Threads.@spawn
    end
    return Fe
end

function update_model_space(M::Array{Determinant,1}, h::Array{T,2}, V::Array{T,4}) where T <: AbstractFloat

    M = complete_set(M)
    H = get_sparse_hamiltonian_matrix(M, h, V, Fermi.CurrentOptions["cas_cutoff"])

    @output "Diagonalizing Hamiltonian...\n"
    decomp, history = partialschur(H, nev=1, tol=10^-12, which=LM())
    Œª, œï = partialeigen(decomp)
    #Œª,œï = eigen(Array(H))

    return Œª[1], œï[:,1], deepcopy(M)
end


function incomp(dets::Array{Determinant,1})

    ming = 0
    for d in dets

        _det = Determinant(d.Œ≤, d.Œ±)
        if !(_det in dets)
            ming += 1
        end
    end

    return ming
end

function complete_set(dets::Array{Determinant,1})

    newdets = Determinant[]
    for d in dets
        
        asym = d.Œ± ‚äª d.Œ≤
        if asym == 0
            continue
        end

        sym = d.Œ± & d.Œ≤

        n = count_ones(asym)
        e = Int(n/2)
        idx = Int[]

        str = repeat("1",e)*repeat("0",e)
        perms = multiset_permutations(str, n)
        
        i = 1
        while i ‚â§ asym
            if 1<<(i-1) & asym ‚â† 0
                push!(idx, i) 
            end
            i += 1
        end

        foo = x->reverse(bitstring(x))[1:7]
        for p in perms
            newŒ± = sym
            newŒ≤ = sym
            for (x,i) in zip(p,idx)
                if x == '1'
                    newŒ± = newŒ± | (1<<(i-1))
                elseif x == '0'
                    newŒ≤ = newŒ≤ | (1<<(i-1))
                end
            end
            push!(newdets, Determinant(newŒ±, newŒ≤))
        end
    end

    return unique(vcat(dets,newdets))
end
